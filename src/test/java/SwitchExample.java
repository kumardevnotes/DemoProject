public class SwitchExample {

    public static void main(String args[]) {
        int empId = 200;
        switch (empId) {
            case 100:
                System.out.println("emp is Kumar");
                break;
            case 101:
                System.out.println("emp is Ayo");
                break;
            case 102:
                System.out.println("emp is Shay");
                break;

        }
    }
}
